// ─── Compare ─────────────────────────────────────────────────────────────────

describe("compareReports", () => {
  it("computes overall delta", () => {
    const reportA = makeReport({ overall_score: 85, grade: "B" });
    const reportB = makeReport({ overall_score: 70, grade: "C" });

    const result = compareReports(reportA, reportB);

    expect(result.overallDelta).toBe(15);
  });

  it("reports both scores and grades", () => {
    const reportA = makeReport({ overall_score: 90, grade: "A" });
    const reportB = makeReport({ overall_score: 60, grade: "C" });

    const result = compareReports(reportA, reportB);

    expect(result.reportA.score).toBe(90);
    expect(result.reportA.grade).toBe("A");
    expect(result.reportB.score).toBe(60);
    expect(result.reportB.grade).toBe("C");
  });

  it("uses custom labels", () => {
    const result = compareReports(makeReport(), makeReport(), "Before", "After");

    expect(result.reportA.label).toBe("Before");
    expect(result.reportB.label).toBe("After");
  });

  it("computes per-domain comparison", () => {
    const reportA = makeReport();
    const reportB = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", score: 60 }),
        makeDomainReport({ domain: "tests", score: 90 }),
      ],
    });

    const result = compareReports(reportA, reportB);
    const secDomain = result.domains.find((d) => d.domain === "security");

    expect(secDomain?.scoreA).toBe(72);
    expect(secDomain?.scoreB).toBe(60);
    expect(secDomain?.delta).toBe(12);
  });

  it("includes domains present in only one report", () => {
    const reportA = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", score: 72 }),
        makeDomainReport({ domain: "conventions", score: 80, findings: [] }),
      ],
    });
    const reportB = makeReport({
      domain_reports: [makeDomainReport({ domain: "security", score: 60 })],
    });

    const result = compareReports(reportA, reportB);
    const convDomain = result.domains.find((d) => d.domain === "conventions");

    expect(convDomain).toBeDefined();
    expect(convDomain?.scoreA).toBe(80);
    expect(convDomain?.scoreB).toBe(100); // defaulted
  });

  it("identifies added findings (in A but not B)", () => {
    const reportA = makeReport({
      domain_reports: [
        makeDomainReport({
          domain: "security",
          findings: [
            makeFinding({ id: "SEC-001" }),
            makeFinding({ id: "SEC-099", title: "New issue" }),
          ],
        }),
      ],
    });
    const reportB = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", findings: [makeFinding({ id: "SEC-001" })] }),
      ],
    });

    const result = compareReports(reportA, reportB);

    expect(result.added).toHaveLength(1);
    expect(result.added[0]?.id).toBe("SEC-099");
  });

  it("identifies removed findings (in B but not A)", () => {
    const reportA = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", findings: [makeFinding({ id: "SEC-001" })] }),
      ],
    });
    const reportB = makeReport({
      domain_reports: [
        makeDomainReport({
          domain: "security",
          findings: [
            makeFinding({ id: "SEC-001" }),
            makeFinding({ id: "SEC-042", title: "Old issue", rule: "old" }),
          ],
        }),
      ],
    });

    const result = compareReports(reportA, reportB);

    expect(result.removed).toHaveLength(1);
    expect(result.removed[0]?.id).toBe("SEC-042");
  });

  it("identifies unchanged findings (present in both)", () => {
    const reportA = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", findings: [makeFinding({ id: "SEC-001" })] }),
      ],
    });
    const reportB = makeReport({
      domain_reports: [
        makeDomainReport({ domain: "security", findings: [makeFinding({ id: "SEC-001" })] }),
      ],
    });

    const result = compareReports(reportA, reportB);

    expect(result.unchanged).toHaveLength(1);
    expect(result.unchanged[0]?.id).toBe("SEC-001");
  });
});

describe("renderComparisonMarkdown", () => {
  it("includes comparison header", () => {
    const result = compareReports(makeReport(), makeReport());
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("## Audit Comparison");
  });

  it("includes summary table with both scores", () => {
    const result = compareReports(
      makeReport({ overall_score: 85, grade: "B" }),
      makeReport({ overall_score: 70, grade: "C" }),
      "v2.0",
      "v1.0",
    );
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("v2.0");
    expect(md).toContain("v1.0");
    expect(md).toContain("85/100");
    expect(md).toContain("70/100");
    expect(md).toContain("+15");
  });

  it("includes domain comparison table", () => {
    const result = compareReports(makeReport(), makeReport());
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("### Domain Comparison");
    expect(md).toContain("Security");
  });

  it("shows added findings", () => {
    const reportA = makeReport({
      domain_reports: [
        makeDomainReport({
          domain: "security",
          findings: [makeFinding({ id: "SEC-099", title: "New vuln" })],
        }),
      ],
    });

    const result = compareReports(reportA, makeReport({ domain_reports: [] }));
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("Added Findings");
    expect(md).toContain("SEC-099");
  });

  it("shows removed findings with strikethrough", () => {
    const reportB = makeReport({
      domain_reports: [
        makeDomainReport({
          domain: "security",
          findings: [makeFinding({ id: "SEC-042", title: "Old vuln" })],
        }),
      ],
    });

    const result = compareReports(makeReport({ domain_reports: [] }), reportB);
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("Removed Findings");
    expect(md).toContain("~~SEC-042");
  });

  it("shows 'None' when no added findings", () => {
    const result = compareReports(makeReport(), makeReport());
    const md = renderComparisonMarkdown(result);

    expect(md).toContain("_None_");
  });
});
